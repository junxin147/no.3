package com.sample;


public class Menu {

  private String feifen;
  private String menu_Id;
  private String farther_Id;
  private String menu_Name;
  private String menu_Path;

  public Menu() {
  }

  public String getFeifen() {
    return feifen;
  }

  public void setFeifen(String feifen) {
    this.feifen = feifen;
  }

  public String getMenu_Id() {
    return menu_Id;
  }

  public void setMenu_Id(String menu_Id) {
    this.menu_Id = menu_Id;
  }

  public String getFarther_Id() {
    return farther_Id;
  }

  public void setFarther_Id(String farther_Id) {
    this.farther_Id = farther_Id;
  }

  public String getMenu_Name() {
    return menu_Name;
  }

  public void setMenu_Name(String menu_Name) {
    this.menu_Name = menu_Name;
  }

  public String getMenu_Path() {
    return menu_Path;
  }

  public void setMenu_Path(String menu_Path) {
    this.menu_Path = menu_Path;
  }
}

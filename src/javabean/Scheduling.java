package com.sample;


public class Scheduling {

  private String staff_name;
  private String scheduling_Id;
  private String staff_Account;
  private String dataday;
  public String getStaff_name() {
    return staff_name;
  }

  public void setStaff_name(String staff_name) {
    this.staff_name = staff_name;
  }

  public String getScheduling_Id() {
    return scheduling_Id;
  }

  public void setScheduling_Id(String scheduling_Id) {
    this.scheduling_Id = scheduling_Id;
  }

  public String getStaff_Account() {
    return staff_Account;
  }

  public void setStaff_Account(String staff_Account) {
    this.staff_Account = staff_Account;
  }

  public String getDataday() {
    return dataday;
  }

  public void setDataday(String dataday) {
    this.dataday = dataday;
  }

}

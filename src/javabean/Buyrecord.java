package com.sample;


public class Buyrecord {

  private String feifen;
  private String id;
  private String moneytime;
  private String matter;
  private String key_Person;
  private String type;
  private String money;
  private String patient_Account;
  private String patient_Name;

  public Buyrecord() {
  }

  public String getPatient_Name() {
    return patient_Name;
  }

  public void setPatient_Name(String patient_Name) {
    this.patient_Name = patient_Name;
  }

  public String getFeifen() {
    return feifen;
  }

  public void setFeifen(String feifen) {
    this.feifen = feifen;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getMoneytime() {
    return moneytime;
  }

  public void setMoneytime(String moneytime) {
    this.moneytime = moneytime;
  }

  public String getMatter() {
    return matter;
  }

  public void setMatter(String matter) {
    this.matter = matter;
  }

  public String getKey_Person() {
    return key_Person;
  }

  public void setKey_Person(String key_Person) {
    this.key_Person = key_Person;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public String getMoney() {
    return money;
  }

  public void setMoney(String money) {
    this.money = money;
  }

  public String getPatient_Account() {
    return patient_Account;
  }

  public void setPatient_Account(String patient_Account) {
    this.patient_Account = patient_Account;
  }
}

package com.sample;


public class ReportTable {

  private String result;
  private String report_Id;
  private String patient_Account;
  private String report_Time;
  private String subject_Domain;
  private String score;
  private String result_Text;

  public ReportTable() {
  }

  public String getResult() {
    return result;
  }

  public void setResult(String result) {
    this.result = result;
  }

  public String getReport_Id() {
    return report_Id;
  }

  public void setReport_Id(String report_Id) {
    this.report_Id = report_Id;
  }

  public String getPatient_Account() {
    return patient_Account;
  }

  public void setPatient_Account(String patient_Account) {
    this.patient_Account = patient_Account;
  }

  public String getReport_Time() {
    return report_Time;
  }

  public void setReport_Time(String report_Time) {
    this.report_Time = report_Time;
  }

  public String getSubject_Domain() {
    return subject_Domain;
  }

  public void setSubject_Domain(String subject_Domain) {
    this.subject_Domain = subject_Domain;
  }

  public String getScore() {
    return score;
  }

  public void setScore(String score) {
    this.score = score;
  }

  public String getResult_Text() {
    return result_Text;
  }

  public void setResult_Text(String result_Text) {
    this.result_Text = result_Text;
  }
}
